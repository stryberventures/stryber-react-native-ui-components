export const progressBar = `## ProgressBar Usage

\`\`\`js
import {ProgressBar} from '@stryberventures/stryber-react-native-ui-components';

() => (
  <ProgressBar />
);
\`\`\`

## Properties

| Prop                                                    | Description                                                                                                    |
| ------------------------------------------------------- | -------------------------------------------------------------------------------------------------------------- |
| **\`type\`**       | Type of ProgressBar: 'inline' or 'steps'                                 |
| **\`value\`**      | Current value of ProgressBar                                 |
| **\`totalValue\`** | Total value of ProgressBar                                 |
| **\`title\`**      | ProgressBar title                                                 |
| **\`infoShowed\`** | Show information about ProgressBar like Title and Value (only for inline ProgressBar)                              |
| **\`size\`**       | ProgressBar size: 'large' or 'small'                                        |`;
